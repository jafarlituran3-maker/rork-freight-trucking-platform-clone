export const generateOffers = (
  weight: number,
  distance: number,
): CarrierOffer[] => {
  return mockCarriers
    .filter((carrier) => carrier.maxCapacity >= weight)
    .map((carrier) => {
      const basePrice = carrier.basePricePerKm * distance;
      const commission = basePrice * (carrier.commission / 100);
      const finalPrice = basePrice + commission;
      const estimatedDuration = distance / carrier.averageSpeed;

      return {
        carrier,
        basePrice,
        commission,
        finalPrice,
        estimatedDistance: distance,
        estimatedDuration,
      };
    })
    .sort((a, b) => a.finalPrice - b.finalPrice);
};
